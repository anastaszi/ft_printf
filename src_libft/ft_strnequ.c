#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if ((s1 != NULL) && (s2 != NULL))
	{
		while ((*s1) && ((int)n > 0))
		{
			if (*s1 != *s2)
				return (0);
			s1++;
			s2++;
			n--;
		}
		if ((*s2 == '\0') || (n == 0))
			return (1);
		else
			return (0);
	}
	return (0);
}
