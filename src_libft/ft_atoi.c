#include "libft.h"

int	ft_atoi(const char *str)
{
	int				n;
	int				m;
	unsigned int	sum;

	n = 1;
	m = 0;
	sum = 0;
	while ((*str >= 9 && *str <= 13) || (*str == ' '))
		str++;
	if ((*str == '+') || (*str == '-'))
	{
		n = (*str == '-') ? -1 : 1;
		str++;
	}
	while (*str == '0')
		str++;
	while ((*str >= '0') && (*str <= '9'))
	{
		sum = sum * 10 + (*str++ - '0');
		m++;
	}
	if (m >= 19)
		sum = (n == -1) ? 0 : -1;
	return (sum * n);
}
